-- Script tạo 3 bảng HR cho hệ thống authentication
-- Bảng sy_user, hr_employee, hr_department

-- Tạo bảng sy_user
CREATE TABLE sy_user (
    USER_NO VARCHAR(20) PRIMARY KEY,
    PERSON_ID VARCHAR(20) NOT NULL,
    CPNY_ID VARCHAR(10) NOT NULL,
    USER_NAME VARCHAR(50) NOT NULL UNIQUE,
    PASSWORD VARCHAR(255) NOT NULL,
    SPECIAL_PARAM VARCHAR(100),
    SPECIAL_VALUE VARCHAR(255),
    USER_TYPE VARCHAR(20),
    PASSWORD_FIRST VARCHAR(255),
    PASSWORD_SECOND VARCHAR(255),
    PASSWORD_THIRD VARCHAR(255),
    CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CREATED_BY VARCHAR(50),
    UPDATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_BY VARCHAR(50),
    ORDERNO INTEGER,
    ACTIVITY INTEGER DEFAULT 1,
    PERSONAL_DATA_FLAG VARCHAR(1),
    PERSONAL_DATA_UPDATE_DATE TIMESTAMP,
    PERSONAL_DATA_UPDATED_IP VARCHAR(50),
    PERSONAL_DATA_UPDATED_BY VARCHAR(50),
    PERSONAL_SEND_DATA_FLAG VARCHAR(1),
    PERSONAL_SEND_DATA_UPDATE_DATE TIMESTAMP,
    PERSONAL_SEND_DATA_UPDATED_IP VARCHAR(50),
    PERSONAL_SEND_DATA_UPDATED_BY VARCHAR(50)
);

-- Tạo bảng hr_employee
